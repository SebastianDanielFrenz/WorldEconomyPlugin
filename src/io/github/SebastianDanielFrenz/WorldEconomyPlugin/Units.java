package io.github.SebastianDanielFrenz.WorldEconomyPlugin;

public class Units {

	public static final int SECOND = 1;
	public static final int MINUTE = 60;
	public static final int HOUR = MINUTE * 60;
	public static final int DAY = HOUR * 24;
	public static final int WEEK = DAY * 7;
	public static final int MONTH = DAY * 30;
	public static final int YEAR = MONTH * 12;

	public static final long KB = 1024;
	public static final long MB = KB * 1024;
	public static final long GB = MB * 1024;
	public static final long TB = GB * 1024;
	public static final long PB = TB * 1024;
	public static final long EB = PB * 1024;
	public static final long YB = EB * 1024;
	public static final long ZB = YB * 1024;

}
